# Igualdad ==

print(5 == 5)
print(5 == 3)
print("Hello" == "Hello")

# Operador de desigualdad != 

print(5 != 5)
print(5 != 3)
print("WOrld" != "WOrld")

# Operador mayor que >

print( 7 > 5 )
print( 7 > 9 )

# Operador menor que <

print( 7 < 5 )
print( 7 < 9 )

# Operador mayor o igual que >=

print( 7 >= 5 )
print( 7 >= 9 )
print( 5 >= 5 )

# Operador menor o igual que <=

print( 7 <= 5 )
print( 7 <= 9 )
print( 5 <= 5 )